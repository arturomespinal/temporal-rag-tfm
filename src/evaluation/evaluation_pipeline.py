import pandas as pd
import numpy as np
import json
from sklearn.metrics import f1_score, roc_auc_score, precision_score, recall_score

def run_full_experiment(rag_system, baseline_model, windows,
                         labels, window_size=50):
    """
    Ejecuta el experimento comparativo completo:
    RAG Temporal vs Baseline sin recuperación.
    Devuelve métricas para ambos sistemas.
    """
    results = {
        'rag': {'scores': [], 'predictions': []},
        'baseline': {'scores': [], 'predictions': []}
    }

    # Usar últimas N ventanas como test
    n_test = len(windows) // 5
    train_windows = windows[:-n_test]
    test_windows = windows[-n_test:]
    test_labels = labels[-n_test:] if labels is not None else None

    # Construir memoria RAG con train
    rag_system.build_memory(train_windows)

    for i, window in enumerate(test_windows):
        # --- Sistema RAG ---
        retrieved, distances, _ = rag_system.retrieve(window)
        rag_score = rag_system.anomaly_score_with_context(
            window, retrieved
        )
        results['rag']['scores'].append(rag_score)

        # --- Baseline (sin RAG) ---
        baseline_emb = rag_system.encoder.predict(
            window.reshape(1, -1), verbose=0
        )
        # Score baseline: distancia al centroide del train
        train_embs = rag_system.encoder.predict(
            train_windows, verbose=0
        )
        centroid = train_embs.mean(axis=0)
        baseline_score = np.linalg.norm(baseline_emb - centroid)
        results['baseline']['scores'].append(baseline_score)

    # Calcular métricas si hay etiquetas
    if test_labels is not None:
        for system in ['rag', 'baseline']:
            scores = np.array(results[system]['scores'])
            threshold = np.percentile(scores, 90)
            y_pred = (scores > threshold).astype(int)
            y_true = np.array(test_labels)

            results[system]['metrics'] = {
                'f1': round(f1_score(y_true, y_pred, zero_division=0), 4),
                'precision': round(precision_score(
                    y_true, y_pred, zero_division=0), 4),
                'recall': round(recall_score(
                    y_true, y_pred, zero_division=0), 4),
                'auc_roc': round(roc_auc_score(y_true, scores), 4)
            }

    return results

def print_comparison_table(results):
    """Imprime tabla comparativa lista para incluir en el TFM"""
    print("\n" + "="*55)
    print(f"{'Métrica':<15} {'RAG Temporal':>15} {'Baseline':>15}")
    print("="*55)
    for metric in ['f1', 'precision', 'recall', 'auc_roc']:
        rag_val = results['rag']['metrics'].get(metric, 'N/A')
        base_val = results['baseline']['metrics'].get(metric, 'N/A')
        print(f"{metric.upper():<15} {rag_val:>15} {base_val:>15}")
    print("="*55)